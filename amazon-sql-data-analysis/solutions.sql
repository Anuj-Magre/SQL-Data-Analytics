/*
15. Inactive Sellers
Identify sellers who haven’t made any sales in the last 6 months.
Challenge: Show the last sale date and total sales from those sellers.
*/

/*We cannot calculate the last 6 months from the current date because 
the last order year in the dataset is 2024. So we will assume the last 
date as 30-07-2024, and any seller who has not made a sale in the last 
6 months from this date will be considered inactive. */


SELECT 
    s.seller_id,
    s.seller_name,
    MAX(o.order_date) AS last_sale_date,
    SUM(oi.total_sale) AS total_sales
FROM sellers s
JOIN orders o 
ON o.seller_id = s.seller_id
JOIN order_items oi
ON oi.order_id = o.order_id
GROUP BY s.seller_id, s.seller_name
HAVING MAX(o.order_date) < (
        SELECT DATE_SUB(MAX(order_date), INTERVAL 6 MONTH)
        FROM orders
);


/*
16. IDENTITY customers into returning or new
if the customer has done more than 5 return categorize them as returning otherwise new
Challenge: List customers id, name, total orders, total returns
*/

SELECT 
    c.customer_id,
    CONCAT(c.first_name,' ',c.last_name) AS customer_name,
    COUNT(o.order_id) AS total_orders,
    SUM(CASE WHEN o.order_status = 'Returned' THEN 1 ELSE 0 END) AS total_returns,
    CASE 
        WHEN SUM(CASE WHEN o.order_status = 'Returned' THEN 1 ELSE 0 END) > 5 
        THEN 'Returning'
        ELSE 'New'
    END AS customer_category
FROM customers c
JOIN orders o
    ON c.customer_id = o.customer_id
GROUP BY c.customer_id, customer_name
ORDER BY total_returns DESC;



