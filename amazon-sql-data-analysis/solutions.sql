/*
1. Top Selling Products
Query the top 10 products by total sales value.
Challenge: Include product name, total quantity sold, and total sales value.
*/

SELECT 
	p.product_id,
	p.product_name,
    count(quantity) as total_quantity_sold,
    round(sum(oi.total_sale),2) as total_sale
FROM order_items oi
JOIN products p
ON p.product_id = oi.product_id
GROUP BY p.product_id
ORDER BY 3 desc
LIMIT 10;



/*
2. Revenue by Category
Calculate total revenue generated by each product category.
Challenge: Include the percentage contribution of each category to total revenue.
*/

SELECT 
	cg.category_name,
    round(sum(oi.total_sale),2) as total_revenue,
    round(
			sum(oi.total_sale) / (select sum(total_sale) from order_items)*100,2
            ) as contribution_pct
FROM order_items oi 
JOIN products p
ON p.product_id = oi.product_id
JOIN category cg
ON cg.category_id = p.category_id
GROUP BY 1
ORDER BY 3 DESC;	



/*
3. Average Order Value (AOV)
Compute the average order value for each customer.
Challenge: Include only customers with more than 5 orders.
*/

SELECT
	c.customer_id,
    concat(c.first_name,' ',c.last_name)as customer_name,
    count(c.customer_id) as total_orders,
    round(avg(oi.total_sale),2) as average_order_value
FROM orders o
JOIN order_items oi
ON o.order_id = oi.order_id
JOIN customers c
ON c.customer_id = o.customer_id
GROUP BY 1
HAVING COUNT(c.customer_id)>5
ORDER BY 3 desc ;


/*
4. Monthly Sales Trend
Query monthly total sales over the past year.
Challenge: Display the sales trend, grouping by month, return current_month sale, last month sale!
*/

WITH monthly_sales as (
	SELECT 
		year(o.order_date) as year,
        month(o.order_date) as month,
        SUM(oi.total_sale) as monthly_sales
	FROM orders o
    JOIN order_items oi
    ON o.order_id = oi.order_id
    GROUP BY 2,1
	)
SELECT 
	year,
    month,
    round(monthly_sales,2) as current_month_sale,
    round(lag(monthly_sales) over(order by year, month),2) as last_month_sale
FROM monthly_sales
ORDER BY 1,2;    
	


