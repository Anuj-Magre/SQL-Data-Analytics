WITH top_seller as (
					SELECT 
						s.seller_id,
						s.seller_name,
						round(sum(oi.total_sale),2) as total_sales_value      
					FROM sellers s
					JOIN orders o
					ON s.seller_id = o.seller_id
					JOIN order_items oi
					ON oi.order_id = o.order_id
					GROUP BY 1,2
					ORDER BY 3 desc
					LIMIT 5
                    ),

seller_reports as (
					SELECT o.seller_id,
						ts.seller_name,
						o.order_status,
						COUNT(*) as total_orders
					FROM orders as o
					JOIN top_seller ts
					ON ts.seller_id = o.seller_id
					WHERE order_status not in ('Inprogress', 'Returned')
					GROUP BY 1,2,3)

SELECT 
	sr.seller_id,
	sr.seller_name,
    tss.total_sales_value,
	SUM(CASE WHEN sr.order_status = 'Completed' THEN sr.total_orders ELSE 0 END) as Completed_orders,
	SUM(CASE WHEN sr.order_status = 'Cancelled' THEN sr.total_orders ELSE 0 END) as Cancelled_orders,
	SUM(sr.total_orders) as total_orders,
	round(SUM(CASE WHEN sr.order_status = 'Completed' THEN sr.total_orders ELSE 0 END)/
	SUM(sr.total_orders)* 100,2) as successful_orders_percentage
FROM seller_reports sr
JOIN top_seller tss
ON tss.seller_id = sr.seller_id
GROUP BY 1, 2;



/*
12. Product Profit Margin
Calculate the profit margin for each product (difference between price and cost of goods sold).
Challenge: Rank products by their profit margin, showing highest to lowest.
*/

SELECT 
	product_id,
	product_name,
    round(profit,2) as profit,
	round(profit_margin,2) as profit_margin,
    rank() over(order by profit_margin desc) as product_ranking
FROM 
	(SELECT 
		p.product_id,
        p.product_name,
        sum(oi.total_sale - (p.cogs * oi.quantity)) as profit,
        sum(oi.total_sale - (p.cogs * oi.quantity)) / sum(oi.total_sale) * 100 as profit_margin
	 FROM products p
	 JOIN order_items oi
	 ON oi.product_id = p.product_id
     GROUP BY 1,2
     ) as t;



