/*
19. Top 10 product with highest decreasing revenue ratio compare to last year(2022) and current_year(2023)
Challenge: Return product_id, product_name, category_name, 2022 revenue and 2023 revenue decrease ratio at end Round the result

Note: Decrease ratio = cr-ls/ls* 100 (cs = current_year ls=last_year)
*/

WITH last_year_sale as (
		SELECT 
			p.product_id,
            p.product_name,
            p.category_id,
            round(SUM(oi.total_sale),2) as revenue
        FROM products p
        JOIN order_items oi
        ON p.product_id = oi.product_id
        JOIN orders o
        ON o.order_id = oi.order_id
		WHERE year(o.order_date)='2022'
        GROUP BY 1,2
        ),

current_year_sale as (
		SELECT 
			p.product_id,
            p.product_name,
            p.category_id,
            round(SUM(oi.total_sale),2) as revenue
        FROM products p
        JOIN order_items oi
        ON p.product_id = oi.product_id
        JOIN orders o
        ON o.order_id = oi.order_id
		WHERE year(o.order_date)='2023'
        GROUP BY 1,2
        )        

SELECT
	cs.product_id,
    cs.product_name,
    ctg.category_name,
	ls.revenue as last_year_revenue_2022,
	cs.revenue as current_year_revenue_2023,
	round(ls.revenue - cs.revenue,2) as rev_diff,
	ROUND((cs.revenue - ls.revenue)/ls.revenue * 100, 2) as reveneue_dec_ratio
FROM last_year_sale as ls
JOIN current_year_sale as cs
ON ls.product_id = cs.product_id
JOIN category ctg
ON ctg.category_id = cs.category_id
WHERE 
	ls.revenue > cs.revenue
ORDER BY reveneue_dec_ratio 
LIMIT 10; 


